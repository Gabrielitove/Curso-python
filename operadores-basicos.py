# OPERACIONES MATEMATICAS

a = 10
b = 3

print(f"{a} y {b}")

print(a + b) # Suma
print(a - b) # Resta
print(a * b) # Multiplicacion
print(a / b) # División (resultado flotante)
print(a // b) # División entera
print(a % b) # Módulo (resto)
print(a ** b) # Potencia

# OPERADORES LOGICOS
x = 5
y = 10

print(f"{x} y {y}")
print(x > y) # False
print(x < y) # True
print(x == y) # False
print(x != y) # True

