# Definiendo una variable con camelCase
nombreCompleto = "Gabriel Velez"

# Definiendo una varibale con snake_case
nombre_completo = "Gabriel Velez"

# Concatenar con +
bienvenida =  "Hola " + "¿Como estas?"

# Concatenar con f-strings
bienvenida = f"Hola {nombre_completo} ¿Como estas?"

